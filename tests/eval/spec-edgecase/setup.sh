#!/usr/bin/env bash
# setup.sh — 경계 규칙이 본문 깊숙이 묻혀 있는 명세 파일 생성
ws="$1"
cat > "${ws}/spec.txt" <<'SPEC'
# 문자열 정규화 명세 v2.1

## 1. 개요
normalize.sh 는 첫 번째 인자로 받은 문자열을 정규화하여
표준 출력에 한 줄로 내보낸다.
    bash normalize.sh <input>

## 2. 기본 동작
영문 대문자를 소문자로 변환한다.
    예: "Hello World" → "hello world"
    예: "ABC" → "abc"

## 3. 전처리
처리 전에 아래 단계를 순서대로 수행한다.

  3-1. 양끝 공백 제거
       입력의 선행(leading)·후행(trailing) ASCII 공백(U+0020)을 모두 제거한다.
       내부 공백은 유지한다.

  3-2. 빈 문자열 처리
       공백 제거 결과가 빈 문자열인 경우 (원래 인자가 ""이거나 공백만 있는
       경우 포함) "EMPTY"를 출력하고 즉시 종료한다. 이후 규칙은 적용하지
       않는다.

## 4. 특수 입력 처리
전처리 완료 후 아래 조건을 순서대로 확인하며, 해당하는 첫 번째 규칙만
적용한다.

  4-1. 하이픈('-') 시작
       전처리 후 문자열의 첫 글자가 ASCII 하이픈('-', U+002D)이면
       "ERR:invalid"를 출력한다. §2 기본 변환은 적용하지 않는다.

  4-2. 숫자 전용 입력
       전처리 후 문자열이 ASCII 숫자(0–9)로만 구성된 경우,
       정수 값으로 파싱하여 선행 0을 제거한 뒤 "NUM:" 접두어를 붙여 출력한다.
           예: "007" → "NUM:7"
           예: "0"   → "NUM:0"
           예: "00"  → "NUM:0"

  4-3. 일반 입력
       위 조건에 해당하지 않으면 §2 기본 동작(소문자 변환)을 적용한다.

## 5. 길이 제한
최종 출력 문자열이 12자를 초과하는 경우 앞 12자만 출력한다.
단, "EMPTY" 및 "ERR:invalid"에는 이 제한을 적용하지 않는다.
NUM: 출력에는 이 제한이 적용된다.
SPEC
